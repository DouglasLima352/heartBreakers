package model;

public class Doctor {

	private int id_doctor;
	private String cpf_doctor, senha_doctor;
	
	public int getId_doctor() {
		return id_doctor;
	}
	
	public void setId_doctor() {
		this.id_doctor = id_doctor;
	}
	
	public String getCpf_doctor() {
		return cpf_doctor;
	}
	
	public void setCpf_doctor() {
		this.cpf_doctor = cpf_doctor;
	}
	
	public String getSenha_doctor() {
		return senha_doctor;
	}
	
	public void setSenha_doctor() {
		this.senha_doctor = senha_doctor;
	}
}
